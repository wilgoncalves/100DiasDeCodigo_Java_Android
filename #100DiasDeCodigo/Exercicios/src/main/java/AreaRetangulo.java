public class AreaRetangulo {
    public static void main(String[] args) {
        // Instanciando os valores da base e da altura do retangulo:
        double base = 10;
        double altura = 5;
        // Multiplicando a base com altura para obter o valor da área:
        double area = base * altura;
        // Imprimindo resultado com formatação de string:
        System.out.printf("A area do retangulo e: %.1f", area);
    }
}
